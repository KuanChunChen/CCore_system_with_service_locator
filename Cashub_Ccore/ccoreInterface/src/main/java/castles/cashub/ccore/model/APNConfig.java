package castles.cashub.ccore.model;

/**
 * A model of app configuration.<br>
 *
 * @author Willy_Chen

 */
public class APNConfig {

    /**
     * The APN id.
     */
    public String APNId;
    /**
     * The Name.
     */
    public String name;
    /**
     * The APN point name.
     */
    public String APNPointName;
    /**
     * The Username.
     */
    public String username;
    /**
     * The Password.
     */
    public String password;
    /**
     * The Proxy.
     */
    public String proxy;
    /**
     * The Port.
     */
    public String port;
    /**
     * The Server.
     */
    public String server;
    /**
     * The Mmsc.
     */
    public String mmsc;
    /**
     * The Mms proxy.
     */
    public String mmsProxy;
    /**
     * The Mms port.
     */
    public String mmsPort;
    /**
     * The Mcc.
     */
    public String mcc;
    /**
     * The Mnc.
     */
    public String mnc;
    /**
     * The APN type.
     */
    public String APNType;
    /**
     * The APN protocol.
     */
    public String APNProtocol;
    /**
     * The APN roaming protocol.
     */
    public String APNRoamingProtocol;


}
